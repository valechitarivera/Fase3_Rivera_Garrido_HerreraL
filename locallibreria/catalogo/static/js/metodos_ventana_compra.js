function Carrito()
{
    var valor_pedido_default=document.getElementById("rdbdefault").value;
    var valor_pedido_perso=document.getElementById("rdbperso").value;
    var tipo_pedido_default=document.getElementById("rdbdefault");
    var tipo_pedido_perso=document.getElementById("rdbperso");
    var cantidad; 
    var precio;
    var total;
    var totalFinal;
    var precioperso;
    cantidad=document.getElementById("cant").value;
    precio= 50000

    total=parseInt(cantidad)*parseInt(precio);
    
    document.getElementById("precio").innerHTML='Precio articulo default: $'+Intl.NumberFormat('es-CL').format(total);
    
        if (tipo_pedido_default.checked)
            {
                totalFinal=total+parseInt(valor_pedido_default);
                document.getElementById("preciofinal").innerHTML='Precio total: $'+Intl.NumberFormat('es-CL').format(totalFinal);
            } 
        else if (tipo_pedido_perso.checked)  
        {
            precioperso=cantidad*valor_pedido_perso;
            totalFinal=total+precioperso;
            document.getElementById("preciofinal").innerHTML='Precio total + Personalizado: $'+Intl.NumberFormat('es-CL').format(totalFinal);
        }
        
    
}




$(document).ready(function()
{
    $('.filtros .itemFiltro[category="all"]').addClass('filtrodefault');     

    $('.itemFiltro').click(function()
    {
    var catProducto=$(this).attr('category');/*valor atributo category*/
    console.log(catProducto);

        $('.itemFiltro').removeClass('filtrodefault');/*remueve clases de elementos*/
        $(this).addClass('filtrodefault');/*anade clases de elementos*/

        $('.productos-items').hide();
        $('.productos-items[category="'+catProducto+'"]').show();
    });
    
    $('.itemFiltro[category="all"]').click(function(){
        $('.productos-items').show();
    });

});

